"""Ejercicio 2: Operadores de asignación

Crea una variable con un número, y usa los operadores +=, -=, *=, /= para modificar su valor paso a paso.
Muestra el valor de la variable después de cada operación."""

numero = 10

numero += 3
print (numero)

numero -= 3
print (numero)

numero *= 3
print (numero)

numero /= 3
print (numero)


